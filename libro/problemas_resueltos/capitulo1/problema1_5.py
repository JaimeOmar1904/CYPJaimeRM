RADIO = float(input("Escribe el radio del cilindro: "))
ALTU = float(input("Escribe la altura del cilindro: "))
VOL = 3.141592 * (RADIO **2) * ALTU
ARE = 2 * 3.141592 * RADIO * ALTU
print(f"El radio del cilindro es de {RADIO}, su altura de {ALTU}, por lo que el resultado del volumen es: {VOL} y el area de {ARE}")

